const input = document.querySelector(".input");
const button = document.querySelector(".btn");

button.addEventListener("click", () => {
  for (let i = 0; i <= input.value; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
      console.log("FizzBuzz");
    } else if (i % 3 === 0) {
      console.log("Fizz");
    } else if (i % 5 === 0) {
      console.log("Buzz");
    } else {
      console.log(i);
    }
  }
});
